#!/usr/bin/env python3
"""
CNP experiment runner.

Generates cnp.jcm for each (n, m, i) configuration, runs JaCaMo,
parses [METRIC] lines, and prints a comparison table.

Usage:
    python3 experiment.py                  # run default matrix
    python3 experiment.py --n 5 --m 10 --i 3   # single run
"""

import argparse
import re
import subprocess
import sys
import textwrap
from collections import defaultdict
from pathlib import Path
from statistics import mean

# ---------------------------------------------------------------------------
# Configuration matrix
# ---------------------------------------------------------------------------

DEFAULT_MATRIX = [
    # label,          n,   m,  i
    ("n2_m5_i1",      2,   5,  1),
    ("n5_m10_i3",     5,  10,  3),   # baseline
    ("n10_m10_i3",   10,  10,  3),
    ("n20_m10_i5",   20,  10,  5),
    ("n5_m3_i3",      5,   3,  3),   # scarce participants
    ("n5_m20_i3",     5,  20,  3),   # abundant participants
]

SERVICES   = ["compute", "storage", "network"]
STRATEGIES = ["random", "fixed", "aggressive", "conservative"]

TIMEOUT_SECONDS = 20   # seconds to wait after JaCaMo starts before killing it

# ---------------------------------------------------------------------------
# JCM generator
# ---------------------------------------------------------------------------

def build_jcm(n: int, m: int, i: int) -> str:
    """
    Generate cnp.jcm with n initiators, m participants, i contracts.

    Service assignment is round-robin so every service appearing on an
    initiator also has at least one participant offering it (guaranteed
    when m >= number of services in use).
    """
    lines = [
        "// Auto-generated by experiment.py",
        f"// n={n} initiators | m={m} participants | i={i} contracts each",
        "",
        "mas cnp {",
        "",
        "    // INITIATORS",
    ]
    contracts = list(range(1, i + 1))
    services_used = set()
    for k in range(1, n + 1):
        svc = SERVICES[(k - 1) % len(SERVICES)]
        services_used.add(svc)
        lines.append(f"    agent in{k} : initiator.asl {{")
        lines.append(f"        beliefs: contracts({contracts}), service({svc})")
        lines.append("    }")
    lines.append("")
    lines.append("    // PARTICIPANTS")
    # Front-load participants with services that initiators actually requested,
    # so small m never starves an initiator (e.g. m=2 with network requests).
    svc_pool = sorted(services_used) or list(SERVICES)
    for k in range(1, m + 1):
        svc   = svc_pool[(k - 1) % len(svc_pool)]
        strat = STRATEGIES[(k - 1) % len(STRATEGIES)]
        lines.append(f"    agent pa{k} : participant.asl {{")
        lines.append(f"        beliefs: service({svc}), strategy({strat})")
        lines.append("    }")
    lines.append("")
    lines.append("}")
    return "\n".join(lines) + "\n"

# ---------------------------------------------------------------------------
# Metrics parser  (same logic as metrics.py, inlined)
# ---------------------------------------------------------------------------

def parse_metrics(output: str) -> dict:
    records = []
    for line in output.splitlines():
        if "[METRIC]" not in line:
            continue
        kv = dict(re.findall(r'(\w+)=(\S+)', line.split("[METRIC]", 1)[1]))
        if not kv:
            continue
        kv["proposals"] = int(kv.get("proposals", 0))
        kv["price"]     = int(kv.get("price", 0)) if "price" in kv else None
        kv["elapsed_ms"] = int(kv.get("elapsed_ms", 0))
        records.append(kv)
    return records

def summarise(records: list, n: int, m: int, i: int) -> dict:
    total   = len(records)
    done    = [r for r in records if r["result"] == "done"]
    fail    = [r for r in records if r["result"] == "fail"]
    timeout = [r for r in records if r["result"] == "timeout"]

    prices   = [r["price"]      for r in done if r["price"]]
    elapsed  = [r["elapsed_ms"] for r in done]
    props    = [r["proposals"]  for r in records if r["proposals"] > 0]

    winner_counts = defaultdict(int)
    for r in done:
        winner_counts[r.get("winner", "?")] += 1
    top_winner = max(winner_counts, key=winner_counts.get) if winner_counts else "n/a"

    return {
        "n": n, "m": m, "i": i,
        "total":        total,
        "done":         len(done),
        "fail":         len(fail),
        "timeout":      len(timeout),
        "success_pct":  round(100 * len(done) / total, 1) if total else 0,
        "price_avg":    round(mean(prices), 1)   if prices  else None,
        "price_min":    min(prices)               if prices  else None,
        "price_max":    max(prices)               if prices  else None,
        "latency_avg":  round(mean(elapsed), 0)  if elapsed else None,
        "latency_max":  max(elapsed)              if elapsed else None,
        "props_avg":    round(mean(props), 2)    if props   else None,
        "top_winner":   top_winner,
    }

# ---------------------------------------------------------------------------
# Runner
# ---------------------------------------------------------------------------

def run_config(label: str, n: int, m: int, i: int, results_dir: Path) -> dict | None:
    jcm_path = Path("cnp.jcm")
    log_path = results_dir / f"{label}.log"

    print(f"\n{'─'*55}")
    print(f"  {label}  (n={n}, m={m}, i={i})  → {n*i} total contracts")
    print(f"{'─'*55}")

    jcm_path.write_text(build_jcm(n, m, i))

    try:
        result = subprocess.run(
            ["./gradlew", "run"],
            capture_output=True, text=True,
            timeout=TIMEOUT_SECONDS,
        )
        output = result.stdout + result.stderr
    except subprocess.TimeoutExpired as e:
        # JaCaMo never self-exits; the timeout kill is expected.
        # Partial output is in the exception attributes.
        stdout = e.stdout if isinstance(e.stdout, str) else (e.stdout or b"").decode("utf-8", errors="replace")
        stderr = e.stderr if isinstance(e.stderr, str) else (e.stderr or b"").decode("utf-8", errors="replace")
        output = stdout + stderr

    log_path.write_text(output)

    records = parse_metrics(output)
    if not records:
        print("  [!] No [METRIC] lines found — check log:", log_path)
        return None

    s = summarise(records, n, m, i)

    print(f"  contracts : {s['done']}/{s['total']} done  ({s['success_pct']}%)")
    if s["price_avg"] is not None:
        print(f"  win price : avg={s['price_avg']}  min={s['price_min']}  max={s['price_max']}")
    if s["latency_avg"] is not None:
        print(f"  latency   : avg={s['latency_avg']}ms  max={s['latency_max']}ms")
    if s["props_avg"] is not None:
        print(f"  proposals : avg={s['props_avg']} per contract")
    print(f"  top winner: {s['top_winner']}")

    return s

# ---------------------------------------------------------------------------
# Table printer
# ---------------------------------------------------------------------------

def print_table(summaries: list[dict]) -> None:
    if not summaries:
        return
    header = (
        f"{'n':>4} {'m':>4} {'i':>3}  "
        f"{'contracts':>10}  "
        f"{'success%':>9}  "
        f"{'avg_price':>9}  "
        f"{'avg_lat_ms':>10}  "
        f"{'props/cnp':>9}  "
        f"{'top_winner'}"
    )
    sep = "─" * len(header)
    print(f"\n{'='*55}")
    print("  COMPARISON TABLE")
    print(f"{'='*55}")
    print(header)
    print(sep)
    for s in summaries:
        print(
            f"{s['n']:>4} {s['m']:>4} {s['i']:>3}  "
            f"{s['done']:>4}/{s['total']:<4}    "
            f"{s['success_pct']:>7.1f}%  "
            f"{str(s['price_avg'] or 'n/a'):>9}  "
            f"{str(int(s['latency_avg']) if s['latency_avg'] else 'n/a'):>10}  "
            f"{str(s['props_avg'] or 'n/a'):>9}  "
            f"{s['top_winner']}"
        )
    print()

# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(description="CNP experiment runner")
    parser.add_argument("--n", type=int, help="Number of initiators (single run)")
    parser.add_argument("--m", type=int, help="Number of participants (single run)")
    parser.add_argument("--i", type=int, help="Contracts per initiator (single run)")
    args = parser.parse_args()

    results_dir = Path("results")
    results_dir.mkdir(exist_ok=True)

    if args.n and args.m and args.i:
        matrix = [(f"n{args.n}_m{args.m}_i{args.i}", args.n, args.m, args.i)]
    else:
        matrix = DEFAULT_MATRIX

    summaries = []
    for label, n, m, i in matrix:
        s = run_config(label, n, m, i, results_dir)
        if s:
            summaries.append(s)

    # Restore baseline cnp.jcm after experiments
    Path("cnp.jcm").write_text(build_jcm(5, 10, 3))

    print_table(summaries)
    print(f"Logs saved to: results/")


if __name__ == "__main__":
    main()
